    let data = DataDeveloper[name];

    function SetContent(){
        // set the content
        GetElement("id" , "dev-text").innerHTML = data.info;
        GetElement("id" , "dev-title").innerHTML = data.title;
        GetElement("id" , "dev-rating-title").innerHTML = data.ratingName;
        for (let i=0; i < GetElement("id" , "dev-rating-strips").children.length; i++){

            if (GetElement("id" , "dev-rating-strips").children[i].classList.contains("bg-dark")){
                GetElement("id" , "dev-rating-strips").children[i].classList.remove("bg-dark");
            }

            if ( (i + 1) <= data.rating ){
                GetElement("id" , "dev-rating-strips").children[i].classList.add("bg-dark");
            }
        }
        GetElement("id" , "dev-img").setAttribute("src" , data.img);
        GetElement("id" , "dev-project-title").innerHTML = data.project.title;
        GetElement("id" , "dev-project-img").setAttribute("src" , data.project.img);
        GetElement("id" , "dev-project-text").innerHTML = data.project.info;
    }
    function GetActiveNav(){
        for (let i=0; i < GetElement("id" , "dev-container-nav").children.length; i++){
            if (GetElement("id" , "dev-container-nav").children[i].classList.contains("title-nav-active")){
                return GetElement("id" , "dev-container-nav").children[i];
            }
        }
    }
    function GetClickedNav(){
        for (let i=0; i < GetElement("id" , "dev-container-nav").children.length; i++){
            if (GetElement("id" , "dev-container-nav").children[i].getAttribute("name") == name){
                return GetElement("id" , "dev-container-nav").children[i];
            }
        }
    }

    if (initiate == true){
        // don't do the animation
        console.log("[SetDataOfDeveloper] ("+name+") initiating");

        // set the content
        SetContent();
    }
