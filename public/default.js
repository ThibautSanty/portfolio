// functions
function GetElement(type, name , element = document){
    if (type == "id"){
        return element.getElementById(name);
    }
}
function SetAnimation(element , type , delay = 0 ){
    let cssClassAnimation = "anim-" + type;
    let cssClassAnimationDelay = "anim-delay-" + delay.toString();
    if (!element.classList.contains(cssClassAnimation)){
        element.classList.add(cssClassAnimation);
        element.classList.add(cssClassAnimationDelay);
    }
    else {
        console.log("[SetAnimation] Animation was already included.", element);
    }
}
